#include <iostream> 

 

using namespace std; 

 

int main() { 

    int first_num, second_num; 

    cout << "Ingrese el primer numero: "; 

    cin >> first_num; 

    cout << "Ingrese el segundo numero: "; 

    cin >> second_num; 

    for (int i = first_num; i <= second_num; i++) { 

        cout << i << endl; 

    } 

    return 0; 

} 
